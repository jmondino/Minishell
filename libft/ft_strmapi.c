#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*str;

	if (s)
	{
		if (!(str = (char *)malloc(sizeof(char) * (ft_strlen(s)) + 1)))
			return (NULL);
		i = 0;
		while (i < ft_strlen(s))
		{
			str[i] = f(i, s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
