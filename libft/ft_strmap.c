#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	size_t	i;

	if (s)
	{
		if (!(str = (char *)malloc(sizeof(char) * (ft_strlen(s)) + 1)))
			return (NULL);
		i = 0;
		while (i < ft_strlen(s))
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
